'use client';

import { useEffect, useState } from 'react';
import Link from 'next/link';

type Poster = {
  _id: string;
  id: string;
  title: string;
  author: string;
  uploadedAt: string;
};

export default function HomePage() {
  const [posters, setPosters] = useState<Poster[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetchPosters();
  }, []);

  async function fetchPosters() {
    try {
      const response = await fetch('/api/posters');
      if (response.ok) {
        const data = await response.json();
        setPosters(data);
      }
    } catch (error) {
      console.error('Error fetching posters:', error);
    } finally {
      setLoading(false);
    }
  }

  return (
    <div className="min-h-screen bg-gray-50">
      <div className="container mx-auto p-4 md:p-8 max-w-6xl">
        <div className="flex items-center justify-between mb-8">
          <h1 className="text-3xl md:text-4xl font-bold">Conference Presentations</h1>
          <Link
            href="/upload"
            className="px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors font-medium"
          >
            + Upload Presentation
          </Link>
        </div>

        {loading ? (
          <div className="text-center py-12">
            <p className="text-gray-600">Loading presentations...</p>
          </div>
        ) : posters.length === 0 ? (
          <div className="text-center py-12 bg-white rounded-lg shadow">
            <p className="text-xl text-gray-600 mb-4">No presentations yet</p>
            <Link
              href="/upload"
              className="inline-block px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors font-medium"
            >
              Upload the First One
            </Link>
          </div>
        ) : (
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
            {posters.map((poster) => (
              <Link
                key={poster._id}
                href={`/view/${poster.id}`}
                className="bg-white rounded-lg shadow-md hover:shadow-xl transition-shadow p-6 border border-gray-200"
              >
                <h2 className="text-xl font-bold mb-2 text-gray-900 line-clamp-2">
                  {poster.title}
                </h2>
                <p className="text-sm text-gray-600 mb-3">
                  by {poster.author}
                </p>
                <p className="text-xs text-gray-500">
                  Uploaded {new Date(poster.uploadedAt).toLocaleDateString()}
                </p>
                <div className="mt-4 text-blue-600 font-medium text-sm">
                  View Presentation â†’
                </div>
              </Link>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}